def ask_with_trace(qa_system, question):
    print("\n================= QA PIPELINE TRACE =================\n")

    # STEP 1
    print("🔹 STEP 1: User Question")
    print("→ The question entered by the user")
    print(question)

    # STEP 2
    expanded = question + " explanation definition causes symptoms effects treatment management"
    print("\n🔹 STEP 2: Query Expansion")
    print("→ Expanding question to improve retrieval")
    print(expanded)

    # STEP 3
    print("\n🔹 STEP 3: Retrieval")
    print("→ Finding most relevant text chunks using embeddings (FAISS)")
    retrieved = qa_system.retrieve(question)

    for i, r in enumerate(retrieved):
        print(f"\nChunk {i+1} | Score: {r['score']:.3f}")
        print(r['chunk'][:200])

    # STEP 4
    full_context = " ".join([c['chunk'] for c in retrieved])
    print("\n🔹 STEP 4: Context Creation")
    print("→ Combining retrieved chunks into a single context")
    print(full_context[:500])

    # STEP 5
    print("\n🔹 STEP 5: Generative Model (T5)")
    print("→ Model generates answer based on context")

    prompt = (
        "Answer using the context.\n\n"
        f"Context:\n{full_context}\n\n"
        f"Question: {question}\nAnswer:"
    )

    inputs = t5_tokenizer(prompt, return_tensors='pt', truncation=True, max_length=1024).to(device)

    with torch.no_grad():
        outputs = t5_model.generate(**inputs, max_new_tokens=150, num_beams=4)

    generated = t5_tokenizer.decode(outputs[0], skip_special_tokens=True)
    print("T5 Output:", generated)

    # STEP 6
    print("\n🔹 STEP 6: Extractive Answer")
    print("→ Extracting most relevant sentences directly from context")
    extractive = qa_system._extractive_answer(question, full_context)
    print(extractive)

    # STEP 7
    print("\n🔹 STEP 7: Answer Selection")
    print("→ Choosing between T5 output and extractive answer")

    overlap = sum(1 for w in generated.lower().split() if w in full_context.lower())
    print("Overlap score:", overlap)

    if overlap >= 5:
        final_answer = generated
        print("Using T5 Answer")
    else:
        final_answer = extractive
        print("Using Extractive Answer")

    # STEP 8
    print("\n🔹 STEP 8: Final Answer Processing")
    print("→ Filtering answer based on question type (e.g., how/what)")

    q = question.lower()
    sentences = nltk.sent_tokenize(final_answer)

    if any(word in q for word in ["how", "manage", "treat"]):
        filtered = []
        for s in sentences:
            if any(w in s.lower() for w in ["manage", "diet", "therapy", "exercise", "insulin", "lifestyle"]):
                filtered.append(s)

        if filtered:
            final_answer = " ".join(filtered)

    final_answer = polish_answer(final_answer)

    print("\n🔹 FINAL ANSWER")
    print(final_answer)

    print("\n================= END =================\n")

    return final_answer

